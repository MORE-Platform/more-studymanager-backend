package io.redlink.more.studymanager.controller.studymanager;

import io.redlink.more.studymanager.model.LoginTokenApplication;
import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Test;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.test.autoconfigure.web.servlet.AutoConfigureMockMvc;
import org.springframework.boot.test.autoconfigure.web.servlet.WebMvcTest;
import org.springframework.http.MediaType;
import org.springframework.test.web.servlet.MockMvc;

import java.util.Arrays;

import static org.hamcrest.Matchers.hasItems;
import static org.hamcrest.Matchers.hasSize;
import static org.springframework.test.web.servlet.request.MockMvcRequestBuilders.get;
import static org.springframework.test.web.servlet.result.MockMvcResultHandlers.print;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.jsonPath;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.status;

@WebMvcTest({ApplicationsAPIV1Controller.class})
@AutoConfigureMockMvc(addFilters = false)
class ApplicationsAPIV1ControllerTest {

    @Autowired
    private MockMvc mvc;

    @Test
    @DisplayName("Get study applications should return all available applications")
    void testGetStudyApplications() throws Exception {
        String[] expectedApplications = Arrays.stream(LoginTokenApplication.values())
                .map(LoginTokenApplication::name)
                .toArray(String[]::new);

        mvc.perform(get("/api/v1/studies/1/application")
                        .contentType(MediaType.APPLICATION_JSON))
                .andDo(print())
                .andExpect(status().isOk())
                .andExpect(jsonPath("$", hasSize(expectedApplications.length)))
                .andExpect(jsonPath("$", hasItems(expectedApplications)));
    }
}
